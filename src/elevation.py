    gdf['angle_deg'] = gdf['slope'].map(lambda x: atan(x) * 360 / (2 * pi))
    gdf.drop(inplace=True, columns=['slope'])
    gdf['angleclass'] = gdf['angle_deg'].map(get_angle_class)
    return gdf
