def revise_angles(gdf):
    assert gdf.crs == "EPSG:4326"
    gdf_0x = gdf.geometry.map(lambda x: x.coords[0][0])
    gdf_0y = gdf.geometry.map(lambda x: x.coords[0][1])
    gdf_1x = gdf.geometry.map(lambda x: x.coords[1][0])
